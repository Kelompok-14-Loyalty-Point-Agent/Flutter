String productName(int id) {
  String valueId = "";
  if (id == 1) {
    valueId = "Data Telkomsel";
  } else if (id == 2) {
    valueId = "Credit Telkomsel";
  } else if (id == 3) {
    valueId = "Data XL";
  } else if (id == 4) {
    valueId = "Credit XL";
  } else if (id == 5) {
    valueId = "Data Smartfren";
  } else if (id == 6) {
    valueId = "Credit Smartfren";
  } else if (id == 7) {
    valueId = "Data Indosat";
  } else if (id == 8) {
    valueId = "Credit Indosat";
  } else if (id == 9) {
    valueId = "Data Axis";
  } else if (id == 10) {
    valueId = "Credit Axis";
  } else if (id == 11) {
    valueId = "Data 3";
  } else if (id == 12) {
    valueId = "Credit 3";
  }
  return valueId;
}
